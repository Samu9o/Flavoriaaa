function interpolate(template: string, params?: Record<string, string | number>) {
  if (!params) return template
  return Object.entries(params).reduce(
    (result, [key, value]) => result.replaceAll(`{${key}}`, String(value)),
    template,
  )
}

export function translate(language: Language, key: string, params?: Record<string, string | number>) {
  const dictionary = translations[language] ?? translations.es
  const fallback = translations.es[key] ?? key
  return interpolate(dictionary[key] ?? fallback, params)
}

const I18nContext = createContext<I18nContextValue | null>(null)

export function LanguageProvider({ children }: { children: React.ReactNode }) {
  const [language, setLanguage] = useState<Language>("es")

  useEffect(() => {
    if (typeof window === "undefined") return
    const saved = window.localStorage.getItem(STORAGE_KEY) as Language | null
    if (saved === "es" || saved === "en" || saved === "fr") {
      setLanguage(saved)
    }
  }, [])

  useEffect(() => {
    if (typeof window === "undefined") return
    window.localStorage.setItem(STORAGE_KEY, language)
    document.documentElement.lang = language
  }, [language])

  const value = useMemo<I18nContextValue>(
    () => ({
      language,
      setLanguage,
      t: (key, params) => translate(language, key, params),
    }),
    [language],
  )

  return <I18nContext.Provider value={value}>{children}</I18nContext.Provider>
}

export function useI18n() {
  const context = useContext(I18nContext)
  if (!context) throw new Error("useI18n debe usarse dentro de LanguageProvider")
  return context
}

export function getLocaleForLanguage(language: Language) {
  if (language === "fr") return "fr-FR"
  if (language === "en") return "en-US"
  return "es-CO"
}
